import turtle
t = turtle.Turtle()
t.pensize(10)
t.speed(10)
t.shape("turtle")
t.color("purple", "black")
t.hideturtle()

t.penup()
t.goto(-150,0)
t.pendown()
t.left(90)
t.forward(100)
t.left(90)
t.circle(50, extent=-180)
t.right(90)
t.forward(100)
t.right(90)
t.circle(-50, extent=-180)

t.penup()
t.goto(-80,100)
t.pendown()
t.forward(100)
t.forward(-50)
t.right(90)
t.forward(200)
t.penup()
t.goto(110,60)
t.pendown()
t.circle(-40, extent=-240)
t.backward(45)
t.circle(45, extent=-220)

t.penup()
t.goto(-90, 120)
t.pendown()
t.forward(-230)
t.right(20)
t.forward(650)
t.right(20)
t.forward(-230)

t.penup()
t.goto(45, 120)
t.pendown()
t.forward(-230)
t.left(20)
t.forward(650)
t.left(20)
t.forward(-230)

turtle.done()

